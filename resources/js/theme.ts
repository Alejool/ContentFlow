export const getGradient = (type: keyof typeof theme.gradients) =>
  theme.gradients[type];
export const getColor = (color: string) => {
  const parts = color.split(".");
  let value: any = theme.colors;
  for (const part of parts) {
    value = value[part];
  }
  return value;
};

